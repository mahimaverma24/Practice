package com.ca.mahima.java.operator;

public class Client {

	String name;
	Integer age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public Client(String name, Integer age) {
		name = this.name;
		age = this.age;
	}

	
}
